console.log("making a while loop displaying 1,2,3,4,5");
var inwhile = 1
while (inwhile <= 5) {
    console.log(inwhile);
    inwhile++
}
console.log("making a do-while loop dsiplaying 1,2,3,4,5")
var inwhile = 1;
do {
    console.log(inwhile);
    inwhile++;
}
while (inwhile <= 5);

console.log("making a for loop displaying 1,2,3,4,5")
for (var inwhile = 1; inwhile <= 5; inwhile++) {
    console.log(inwhile)
}